    <div>
        <h3 style="font-weight: 900;">Usage</h3>
        <h4>Plant medicines are in wide use around the world. In most of the developing world, especially in rural areas, local traditional medicine, including herbalism, is the only source of health care for people, while in the developed world, alternative medicine including use of dietary supplements is marketed aggressively using the claims of traditional medicine. As of 2015, most products made from medicinal plants had not been tested for their safety and efficacy, and products that were marketed in developed economies and provided in the undeveloped world by traditional healers were of uneven quality, sometimes containing dangerous contaminants. Traditional Chinese medicine makes use of a wide variety of plants, among other materials and techniques. Researchers from Kew Gardens found 104 species used for diabetes in Central America, of which seven had been identified in at least three separate studies. The Yanomami of the Brazilian Amazon, assisted by researchers, have described 101 plant species used for traditional medicines. 
            Drugs derived from plants including opiates, cocaine and cannabis have both medical and recreational uses. Different countries have at various times made use of illegal drugs, partly on the basis of the risks involved in taking psychoactive drugs. 
            </h4> 
